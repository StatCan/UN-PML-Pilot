MAX_NUMBER_OF_CLIENTS="$1"
BENCHMARK_RUN_NAME="$3"

echo "Running benchmark for number of clients with max $MAX_NUMBER_OF_CLIENTS clients"

for ((NUMBER_OF_CLIENTS=1; NUMBER_OF_CLIENTS<=MAX_NUMBER_OF_CLIENTS;NUMBER_OF_CLIENTS++))
do
    BENCHMARK_ITERATION_NAME="${BENCHMARK_RUN_NAME}_${NUMBER_OF_CLIENTS}"
    bash launch_benchmark_run.sh $NUMBER_OF_CLIENTS $BENCHMARK_ITERATION_NAME
done


